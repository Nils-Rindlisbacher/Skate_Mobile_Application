// A base class for all localizations
abstract class AppLocalizations {
  static final Map<String, AppLocalizations> _localizations = {};

  factory AppLocalizations.of(String locale) {
    if (_localizations.isEmpty) {
      _localizations['de'] = const AppLocalizationsDe();
      _localizations['en'] = const AppLocalizationsEn();
      _localizations['es'] = const AppLocalizationsEs();
      _localizations['it'] = const AppLocalizationsIt();
      _localizations['fr'] = const AppLocalizationsFr();
    }
    return _localizations[locale] ?? const AppLocalizationsDe();
  }

  String get homePageTitle;
  String get profileMenuItem;
  String get progressTrackerMenuItem;
  String get spotMapMenuItem;
  String get settingsMenuItem;
  String get trickListMenuItem;
  String get leaderboardMenuItem;
  String get language;
  String get english;
  String get german;
  String get italian;
  String get french;
  String get spanish;
  String get username;
  String get name;
  String get password;
  String get email;
  String get loginPageTitle;
  String get loginButton;
  String get registerButton;
  String get logoutButton;
  String get all;
  String get loginRequiredWarning;
  String get guest;
  
  // New strings for full app localization
  String get tricksCompleted;
  String get tricks;
  String get complete;
  String get noTricksCompleted;
  String get wishlist;
  String get wishlistEmpty;
  String get welcomeMessage;
  String get enterCredentials;
  String get registrationSuccess;
  String get registrationFailed;
  String get loginFailed;
  String get enterName;
  String get enterUsername;
  String get enterEmail;
  String get passwordTooShort;
  String get recentActivity;
  String get noActivity;
  String get loginNow;
  String get emailAlreadyExists;

  // Strings for trick selection
  String get selectInitialTricksTitle;
  String get selectInitialTricksSubtitle;
  String get saveAndContinue;
  String get searchTricks;
  String get errorLoadingTricks;
  String get errorSaving;

  // Privacy and Public Profile
  String get publicProfile;
  String get publicProfileSubtitle;
  String get privateProfile;
  String get profileIsPrivate;
  String get profileVisibility;

  // Profile Page Strings
  String get recentlyCompleted;
  String get noTricksYet;
  String get profilePictureUpdated;
  String get failedToUpload;
  String get loadingData;
  String get noUsersFound;

  // Account management
  String get deleteAccount;
  String get deleteAccountConfirm;
  String get deleteAccountWarning;
  String get cancel;
}

